export const setTradingName = (name: any) => ({
    type: 'SET_TRADING_NAME',
    payload: name,
  });
  
  export const setSameAsRegistered = (value: boolean) => ({
    type: 'SET_SAME_AS_REGISTERED',
    payload: value,
  });
  
  export const setTradingAddress = (address: any) => ({
    type: 'SET_TRADING_ADDRESS',
    payload: address,
  });
  